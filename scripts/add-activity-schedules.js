import mongoose from "mongoose";
import dotenv from "dotenv";
import { activityModel } from "../src/DB/Model/Activity.model.js";
import { activityScheduleModel } from "../src/DB/Model/ActivitySchedule.model.js";

dotenv.config({ path: "./src/config/.env.dev" });

const START_SLOTS = [
  { startTime: "09:00", endTime: "10:30" },
  { startTime: "12:00", endTime: "13:30" },
  { startTime: "15:00", endTime: "16:30" },
];

function toUtcDateOnly(date) {
  const d = new Date(date);
  d.setUTCHours(0, 0, 0, 0);
  return d;
}

async function main() {
  if (!process.env.DB_URL) {
    throw new Error("DB_URL is missing in ./src/config/.env.dev");
  }

  await mongoose.connect(process.env.DB_URL);

  try {
    const activities = await activityModel.find({}, "_id title defaultCapacity basePrice").lean();

    if (activities.length === 0) {
      console.log("No activities found. Nothing to seed.");
      return;
    }

    let totalCreated = 0;
    const today = toUtcDateOnly(new Date());

    for (const activity of activities) {
      const existingCount = await activityScheduleModel.countDocuments({ activity: activity._id });
      const capacity = Math.max(1, Number(activity.defaultCapacity ?? 1));
      const basePrice = Number(activity.basePrice ?? 0);

      const docsToInsert = START_SLOTS.map((slot, idx) => {
        const date = new Date(today);
        // Always append new sessions after current count to avoid unique collisions.
        date.setUTCDate(today.getUTCDate() + existingCount + idx + 1);

        return {
          activity: activity._id,
          date,
          startTime: slot.startTime,
          endTime: slot.endTime,
          capacity,
          availableSeats: capacity,
          priceOverride: basePrice,
          status: "scheduled",
          notes: "Auto-generated by add-activity-schedules script",
        };
      });

      const created = await activityScheduleModel.insertMany(docsToInsert, { ordered: true });
      totalCreated += created.length;

      console.log(
        `Activity "${activity.title}": created ${created.length} schedules (had ${existingCount}, now ${existingCount + created.length}).`
      );
    }

    console.log(`Done. Created ${totalCreated} schedules across ${activities.length} activities.`);
  } finally {
    await mongoose.disconnect();
  }
}

main().catch((error) => {
  console.error("Failed to add activity schedules:", error.message);
  process.exit(1);
});
